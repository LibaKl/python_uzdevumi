#while loop

count=1
while count < 12:
    print(f"Skaits ir: {count}")
    count += 2
else:
    print("Katrs otrais saskaitīts")

#for loop
numbers = [1, 3, 5, 7, 9, 11]  # Saraksts ar skaitļiem
for count in numbers:
    print(f"Skaits ir: {count}")
else:
    print("Visi saskaitīti")



#range loop
for count in range(1, 12, 2): 
    print(f"Skaits ir: {count}")
else:
    print("Katrs otrais saskaitīts")


